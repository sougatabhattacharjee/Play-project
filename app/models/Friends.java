public class Friends {
	
	public String name;
	public String friendtype;
	public String dbid;
	public String sname;
	
	public Friends() {
		super();
	}

	public Friends(String name, String friendtype, String dbid, String sname) {
		super();
		this.name = name;
		this.friendtype = friendtype;
		this.dbid = dbid;
		this.sname = sname;
	}
	
	
	
}
